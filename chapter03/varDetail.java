public class varDetail {
	public static void main (String[] args){
		/*
			变量细节
				1. 变量必须先声明，再使用；
				2. String 必须大写；
				3. 赋的值必须符合声明的类型；
				4. 同一个作用域，变量不能重名；
				5. char，字符类型，赋的值只能是单个字符；
				6. 变量 = 数据类型 + 变量名 + 值；
				7. 变量表示内存中的一个存储区域[不同的变量、类型，占用的空间大小不同]；
		*/
		String a = "0sdf";
		System.out.println(a);
	}
}